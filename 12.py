# Aspectos-chave na nomenclatura de variáveis em Python são:

# - Algumas palavras são palavras-chave reservadas 
# (por exemplo, for, while, True). 
# Elas têm um significado especial em Python, 
# então você não pode usá-las como nomes de variáveis.
# - Nomes de variáveis não podem começar com um número, 
# e só podem conter caracteres alfanuméricos ou sublinhados.
# - Nomes de variáveis são sensíveis a maiúsculas e minúsculas, 
# ou seja, my_var é diferente de my_Var e MY_VAR.
# - Finalmente, é uma convenção comum escrever nomes de variáveis 
# usando snake_case, onde cada espaço é substituído por 
# um caractere de sublinhado e 
# as palavras são escritas em letras minúsculas.

